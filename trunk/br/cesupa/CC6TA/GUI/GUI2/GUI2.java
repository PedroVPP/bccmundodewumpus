    public void preencherTextAreaEsquerdo() {
        for (int i = partida.getMapa().getLinhas() - 1; i >= 0; i--) {
            for (int j = 0; j < partida.getMapa().getColunas(); j++) {
                jtEsquerdo.append("[");

                if (mapa.getMapa()[i][j].isBrisa()) {
                    jtEsquerdo.append("b ");
                }
                if (mapa.getMapa()[i][j].isResplendor()) {
                    jtEsquerdo.append("r ");
                }
                if (mapa.getMapa()[i][j].isFedor()) {
                    jtEsquerdo.append("f ");
                }
                if (mapa.getMapa()[i][j].getObjetoDoTerreno() != null) {
                    jtEsquerdo.append(mapa.getMapa()[i][j].getObjetoDoTerreno().getTipoObjeto().toString());
                }

                jtEsquerdo.append("]\t");
            }
            jtEsquerdo.append("\n");
        }
    }

    public void preencherTextAreaDireito() {


        for (int i = partida.getMapa().getLinhas() - 1; i >= 0; i--) {
            for (int j = 0; j < partida.getMapa().getColunas(); j++) {
                jtDireito.append("[");

                if (mapa.getMapa()[i][j].isPossibilidadeDeBuraco()) {
                    jtDireito.append("pb ");
                }
                if (mapa.getMapa()[i][j].isPossibilidadeDeWumpus()) {
                    jtDireito.append("pw ");
                }
                if (mapa.getMapa()[i][j].isCasaSegura()) {
                    jtDireito.append("OK ");
                }
                if (mapa.getMapa()[i][j].isVisitadoPeloAgente()) {
                    jtDireito.append("V ");
                }
                if (mapa.getMapa()[i][j].getObjetoDoTerreno() != null) {
                    jtDireito.append(mapa.getMapa()[i][j].getObjetoDoTerreno().getTipoObjeto().toString());
                }

                jtDireito.append("]\t");
            }
            jtDireito.append("\n");
        }
    }

    public void preencherTextAreaBaixo() {
        jtBaixo.append(fachada.getAgente().getHistorico().getHistorico());
    }

