    private void iniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_iniciarActionPerformed

        int buraco1PosicaoX = Integer.parseInt(textFieldBuraco1PosicaoX.getText());
        int buraco1PosicaoY = Integer.parseInt(textFieldBuraco1PosicaoY.getText());

        int buraco2PosicaoX = Integer.parseInt(textFieldBuraco2PosicaoX.getText());
        int buraco2PosicaoY = Integer.parseInt(textFieldBuraco2PosicaoY.getText());

        int buraco3PosicaoX = Integer.parseInt(textFieldBuraco3PosicaoX.getText());
        int buraco3PosicaoY = Integer.parseInt(textFieldBuraco3PosicaoY.getText());

        int ouroPosicaoX = Integer.parseInt(textFieldOuroPosicaoX.getText());
        int ouroPosicaoY = Integer.parseInt(textFieldOuroPosicaoY.getText());

        int wumpusPosicaoX = Integer.parseInt(textFieldWumpusPosicaoX.getText());
        int wumpusPosicaoY = Integer.parseInt(textFieldWumpusPosicaoY.getText());

        fachada.criaBuraco(buraco1PosicaoX, buraco1PosicaoY);
        fachada.criaBuraco(buraco2PosicaoX, buraco2PosicaoY);
        fachada.criaBuraco(buraco3PosicaoX, buraco3PosicaoY);

        fachada.criaWumpus(wumpusPosicaoX, wumpusPosicaoY);

        fachada.criaOuro(ouroPosicaoX, ouroPosicaoY);

        fachada.iniciarPartida();

        preencherTextAreaEsquerdo();
        preencherTextAreaDireito();
        preencherTextAreaBaixo();

        textFieldPontuacao.setText(""+fachada.getAgente().getPontuacao());
    }//GEN-LAST:event_iniciarActionPerformed

